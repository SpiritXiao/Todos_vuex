<template>
          <li @mouseenter="handleEnter(true)" @mouseleave="handleEnter(false)" :style='{background:bgColor}'>
          <label>
            <input type="checkbox" v-model="todo.complete"/>
            <span>{{todo.title}}</span>
          </label>
          <button class="btn btn-danger" v-show='isHover' @click='deleteItem'>删除</button>
        </li>
</template>

<script>
export default {
props:{
  todo:Object,
  index:Number,
},
data() {
  return {
    bgColor:'white',
    isHover:false
  }
},
methods: {
  handleEnter(isEnter){
    if(isEnter){
      this.bgColor='gray',
      this.isHover=true
    }else {
      this.bgColor='white',
      this.isHover=false
    }
  },
  deleteItem(){
    const {todo,index}=this
    if(window.confirm(`确定删除${todo.title}吗？`)){
      this.$store.dispatch('deleteTodo',index)
    }
  }
},
}
</script>

